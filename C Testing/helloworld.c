#include <stdio.h>

int main(void) {
    printf("Hello World\n");
    return 0;
}

// In the code above, the #include <stdio.h> line is used to include the header file for the Standard Input/Output library, which provides access to the printf function used to output the "Hello World" string.

// The int main(void) block defines the main function, which is the entry point of the program. Inside the function, the printf function is called with the string "Hello World\n" as an argument, which outputs the string to the console. The \n character at the end of the string is a newline character, which causes the output to appear on a new line.

