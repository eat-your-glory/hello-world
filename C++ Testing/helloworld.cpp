#include <iostream>

int main() {
    std::cout << "Hello World" << std::endl;
    return 0;
}

// In the code above, the #include <iostream> line is used to include the header file for the input/output stream library, which provides access to the cout object used to output the "Hello World" string.

// The int main() block defines the main function, which is the entry point of the program. Inside the function, the cout object is used to output the "Hello World" string to the console. The endl manipulator is used to append a newline character to the end of the string, so the output will appear on a new line.

