package main

import "fmt"

func main() {
    fmt.Println("Hello World")
}

// In the code above, the package main statement indicates that the code in the file belongs to the main package, which is the entry point for a Go program.

// The import "fmt" statement is used to import the fmt package, which provides access to the Println function used to output the "Hello World" string.

// The func main() block defines the main function, which is the starting point of the program. Inside the function, the fmt.Println function is called with the string "Hello World" as an argument, which outputs the string to the console.

